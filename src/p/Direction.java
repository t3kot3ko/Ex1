package p;

public class Direction {
	private int _dx;
	private int _dy;
	private int _dz;
	
	public int getDx() {
		return _dx;
	}
	public void setDx(int dx) {
		this._dx = dx;
	}
	
	public int getDy() {
		return _dy;
	}
	public void setDy(int dy) {
		this._dy = dy;
	}
	public int getDz() {
		return _dz;
	}
	public void setDz(int dz) {
		this._dz = dz;
	}
}
